class Pattern5
{
    public static void main(String args[])
    {
          int c=0;
        for(int i =1;i<=3;i++)
        {
            for(int j=1;j<=5;j++)
            {
             
              c++;
            System.out.printf("%02d  ",c);
            }
            System.out.println("  ");   
        }
              
    }
}