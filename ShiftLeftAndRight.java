class ShiftLeftAndRight
{
    public static void main(String args[])
           
    {
            int a=10;
            int y=a<<2;//left shift by two
            int x=a<<1;//left shift by one
            int e=a>>2;//right shift by two
            int z=a>>1;//right shift by one
            //also you can any number of time you want for ex-1,2,3,4,5,6 and so on
            System.out.println("the shift of the given number are");
            System.out.println("left shift is : "+x);
            System.out.println("right shift is : "+z);
            System.out.println("right shift by 2 is : "+e);
            System.out.println("left shift by 2 is : "+y);
    }
}